package day3;

public class OperatorsDemo4 {

    public static void main(String[] args) {
        //4.Relational Operator
        //>< >= == !=
        int a=4;
        int b=5;
        System.out.println(a>b); //F
        System.out.println(a<b); //T
        System.out.println(a>=b); //F
        System.out.println(a<=b); //T
        System.out.println(a==b); //F
        System.out.println(a!=b);//T



    }
}
